package Operator;
public class OperatorIncDec {
    public static void main(String[] args){
    int x;
    x = 23;
    System.out.println("perintah Increment");
    System.out.println("++x hasilnya: " + ++x);
    System.out.println("Nilai x: " + x);
    System.out.println("x++ hasilnya: " + x++);
    System.out.println("Nilai x: " + x);
    
    System.out.println("perintah Decrement");
    System.out.println("--x hasilnya: " + --x);
    System.out.println("Nilai x: " + x);
    System.out.println("x-- hasilnya: " + x--);
    System.out.println("Nilai x: " + x);
    }
}
