package Operator;
public class OperatorRelasional {
    public static void main(String[] args) {
    int x, y, z;
    x = 3;
    y = 5;
    z =4;
    System.out.println("Operator ==");
    System.out.println("x == y? " + (x == y));
    System.out.println("x == z? " + (x == z));
    System.out.println("Operator !=");
    System.out.println("x != y? " + (x != y));
    System.out.println("x != z? " + (x != z));
    System.out.println("Operator >");
    System.out.println("x > y? " + (x > y));
    System.out.println("x > z? " + (x > z));
    System.out.println("Operator <");
    System.out.println("x < y? " + (x < y));
    System.out.println("x < z? " + (x < z));
    System.out.println("Operator >=");
    System.out.println("x >= y? " + (x >= y));
    System.out.println("x >= z? " + (x >= z));
    System.out.println("Operator <=");
    System.out.println("x <= y? " + (x <= y));
    System.out.println("x <= z? " + (x <= z));
    }
}
